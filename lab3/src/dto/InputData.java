package dto;

public class InputData {
    private double a;
    private double b;
    private double e;

    public InputData(double a, double b, double e) {
        this.a = a;
        this.b = b;
        this.e = e;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getE() {
        return e;
    }
}